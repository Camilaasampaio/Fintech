package br.com.cabank.bean;

public class Imagem {

    private int codigoImagem;
    private String descricaoImagem;
    private String descricaoCaminho;

    public int getCodigoImagem() {
        return codigoImagem;
    }

    public void setCodigoImagem(int codigoImagem) {
        this.codigoImagem = codigoImagem;
    }

    public String getDescricaoImagem() {
        return descricaoImagem;
    }

    public void setDescricaoImagem(String descricaoImagem) {
        this.descricaoImagem = descricaoImagem;
    }

    public String getDescricaoCaminho() {
        return descricaoCaminho;
    }

    public void setDescricaoCaminho(String descricaoCaminho) {
        this.descricaoCaminho = descricaoCaminho;
    }
}